                        Risks and Complications:
                    </h3>
                    <p>
                        A small percentage of the fat cells do not survive the transfer process and are subsequently
                        reabsorbed by the body. An analysis of our MRI (nuclear spin) scans taken prior to and six
                        months following the operations has shown that more than 75% of the fat cells transplanted using the BEAULI<sup>™</sup> method “take” and survive. The main risk associated with the use of fat grafting for breast augmentation is the absorption of fat cells that do not survive the procedure. This can lead to the development of oil cysts if a large number of fat cells clump together, and the body is therefore unable to absorb them effectively. This problem can be largely avoided by using the BEAULI<sup>™</sup> technique, through which the fat cells are finely distributed in the tissue. In a US study involving 70 patients small calcifications were reported in three to four percent of the patients at their two-year check-up. Mammography results, however, clearly identified these calcifications as benign. In rare cases an infection may develop (immediately following the treatment). Such infections generally respond well to antibiotics. In extremely severe cases the procedure could result in the formation of an abscess requiring surgical treatment or even in breast mutilation. These risks, however, are associated with any surgical treatment involving the breast, even with breast enlargement using silicone implants. To date we have not observed any of these complications in patients treated using our method, which represents a new and genuine alternative to silicone implants. The main advantages are the natural appearance of the treated breasts and the absence of visible scars. Moreover the fat grafting procedure is associated with considerably less pain.
